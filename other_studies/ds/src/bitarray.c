bitarr_ty SetAll()
{
	return ~0U;
}

bitarr_ty ResetAll()
{
	return 0U;
}

bitarr_ty SetOn(bitarr_ty arr, size_t index)
{
	size_t mask = 1U;
	
	return arr | (mask << index);
}

bitarr_ty SetOff(bitarr_ty arr, size_t index) /* TODO: arr & ~(mask << position) - use ~ is a good method! */
{
	size_t mask = 1U;
	
	return arr ^ (((arr >> index) & mask) << index);
}

bitarr_ty SetBit(bitarr_ty arr, size_t index, int boolean_value)
{
	switch (boolean_value)
	{
	case 0:
		return SetOff(arr, index);
	case 1:
		return SetOn(arr, index);
	default:
		fprintf(stderr, "%s", "Not such value...\n");
		return FAIL;
	}
}

int GetVal(bitarr_ty arr, size_t index)
{
	return (arr >> index) & 1U;
}

int BitIsOff(bitarr_ty arr, size_t index)
{
	return (0 == GetVal(arr, index));
}

bitarr_ty FlipBit(bitarr_ty arr, size_t index)
{
	int val = GetVal(arr, index);

	return SetBit(arr, index, !val); /* TODO: do with mask. the ! is not good */ 
}

