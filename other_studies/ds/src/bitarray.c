bitarr_ty RotR(bitarr_ty arr, size_t shift)
{
	size_t mask = ~0U;
	
	mask <<= (sizeof(size_t) * BYTE) - shift;
	return (arr << shift) | ((arr & mask) >> ((sizeof(size_t) * BYTE) - shift));
}

bitarr_ty RotL(bitarr_ty arr, size_t shift)
{
	size_t mask = ~0U;
	
	mask >>= (sizeof(size_t) * BYTE) - shift;
	return (arr >> shift) | ((arr & mask) << ((sizeof(size_t) * BYTE) - shift));
}

size_t CountOn(bitarr_ty arr)
{
	size_t count = 0;

	while (0 < arr)
	{
		arr &= arr - 1;
		++count;
	}
	
	return count;
}

size_t CountOff(bitarr_ty arr)
{
	return (sizeof(size_t) * BYTE) - CountOn(arr);
}

